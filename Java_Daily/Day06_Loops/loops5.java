package Java_Daily.Day06_Loops;

public class loops5 {
    public static void main(String[] args) {
        int size = 4;
        for (int i = 1; i<= size; i++) {
            for (int j = 1; j <= size; j++) {
                System.out.print(j + "\t");
            }
            System.out.println();
        }
    }
    
}
